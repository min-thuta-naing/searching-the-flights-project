/**
 * Get season configuration for a destination
 * Priority: 1. provinceConfigs (specific province config) > 2. regionConfigs (by region) > 3. defaultConfig
 */
/**
 * Normalize province value for season lookup (bangkok-dmk/bkk -> bangkok)
 */
function normalizeProvinceForSeason(province: string): string {
  if (province.startsWith('bangkok-')) {
    return 'bangkok'
  }
  return province
}

export function getSeasonConfig(destination: string): SeasonConfig {
  // ‡πÉ‡∏ä‡πâ‡∏ä‡∏∑‡πà‡∏≠‡∏à‡∏±‡∏á‡∏´‡∏ß‡∏±‡∏î‡πÄ‡∏õ‡πá‡∏ô‡∏ï‡∏±‡∏ß‡∏û‡∏¥‡∏°‡∏û‡πå‡πÄ‡∏•‡πá‡∏Å‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤
  let normalizedDestination = destination.toLowerCase().replace(/ /g, '-')
  
  // Normalize Bangkok airports to 'bangkok' for lookup
  normalizedDestination = normalizeProvinceForSeason(normalizedDestination)
  
  // 1. ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡∏°‡∏µ config ‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡∏à‡∏±‡∏á‡∏´‡∏ß‡∏±‡∏î‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà (priority ‡∏™‡∏π‡∏á‡∏™‡∏∏‡∏î)
  if (provinceConfigs[normalizedDestination]) {
    return provinceConfigs[normalizedDestination]
  }
  
  // 2. ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤ destination ‡∏≠‡∏¢‡∏π‡πà‡πÉ‡∏ô region ‡πÑ‡∏´‡∏ô
  const region = provinceToRegion[normalizedDestination]
  if (region && regionConfigs[region]) {
    return regionConfigs[region]
  }
  
  // 3. ‡πÉ‡∏ä‡πâ default config
  return defaultConfig
}

/**
 * Get current season type for a destination based on month
 * Uses static season configuration from getSeasonConfig()
 */
export function getCurrentSeasonFromConfig(
  destination: string,
  date?: Date
): 'high' | 'normal' | 'low' {
  const targetDate = date || new Date()
  const month = targetDate.getMonth()
  const monthName = thaiMonthsFull[month]
  
  const seasonConfig = getSeasonConfig(destination)
  
  // Check which season this month belongs to (priority: high > normal > low)
  if (seasonConfig.high.months.includes(monthName)) {
    return 'high'
  }
  if (seasonConfig.low.months.includes(monthName)) {
    return 'low'
  }
  // Default to normal if not in high or low
  return 'normal'
}

